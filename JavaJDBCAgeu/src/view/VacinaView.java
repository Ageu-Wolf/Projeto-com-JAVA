    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        try {
            int doses = Integer.parseInt(jTextFieldDoses.getText());
            if (vacinaController.create(
                jTextFieldNome.getText(),
                jTextFieldFabricante.getText(),
                doses)) {
                JOptionPane.showMessageDialog(this, "Vacina cadastrada com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar vacina!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Informe um número válido de doses!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        getListaVacinas();
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            int doses = Integer.parseInt(jTextFieldDoses.getText());
            if (vacinaController.update(
                idVacina,
                jTextFieldNome.getText(),
                jTextFieldFabricante.getText(),
                doses)) {
                JOptionPane.showMessageDialog(this, "Vacina atualizada com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar vacina!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Número de doses inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        getListaVacinas();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (vacinaController.delete(idVacina)) {
            JOptionPane.showMessageDialog(this, "Vacina excluída com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir vacina!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        getListaVacinas();
    }//GEN-LAST:event_btnExcluirActionPerformed

