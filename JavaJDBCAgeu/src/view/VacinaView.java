    private void preencherCampos(Vacinabean vacina) {
        this.idVacina = vacina.getIdVacina();
        jTextFieldNome.setText(vacina.getNome());
        jTextFieldFabricante.setText(vacina.getFabricante());
        jTextFieldDoses.setText(String.valueOf(vacina.getDosesNecessarias()));
        btnIncluir.setEnabled(false);
        btnExcluir.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnImprimir.setEnabled(true);
        this.alterar = true;
    }

    private void limpaCampos() {
        jTextFieldNome.setText("");
        jTextFieldFabricante.setText("");
        jTextFieldDoses.setText("");
    }

