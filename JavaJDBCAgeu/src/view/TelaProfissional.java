    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
   if (profissionalController.create(
                jTextFieldRegistroProfissional.getText(),
                jTextFieldNome.getText(),
                jFormattedTextFieldCpf.getText())) {
            JOptionPane.showMessageDialog(this, "Profissional salvo com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar o profissional!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        getListaProfissionais();
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (profissionalController.update(
                idProfissional,
                jTextFieldRegistroProfissional.getText(),
                jTextFieldNome.getText(),
                jFormattedTextFieldCpf.getText())) {
            JOptionPane.showMessageDialog(this, "Profissional alterado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar o profissional!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        getListaProfissionais();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
    if (profissionalController.delete(idProfissional)) {
            JOptionPane.showMessageDialog(this, "Profissional excluÃ­do com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir o profissional!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        getListaProfissionais();
    }//GEN-LAST:event_btnExcluirActionPerformed

