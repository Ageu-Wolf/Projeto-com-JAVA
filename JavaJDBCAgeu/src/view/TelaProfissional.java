        private void preencheCampos(Profissionalbean profissional) {
        this.idProfissional = profissional.getIdProfissional();
        jTextFieldNome.setText(profissional.getNome());
        jFormattedTextFieldCpf.setText(profissional.getCpf());
        jTextFieldRegistroProfissional.setText(profissional.getRegistroprofissional());
        btnIncluir.setEnabled(false);
        btnExcluir.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnImprimir.setEnabled(true);
        this.alterar = true;
    }
    private void getListaProfissionais() {
        ConsultaProfissional consulta = new ConsultaProfissional();
        consulta.setVisible(true);
        this.dispose();
    }
    private void limpaCampos() {
        jTextFieldNome.setText("");
        jFormattedTextFieldCpf.setText("");
        jTextFieldRegistroProfissional.setText("");
    }


