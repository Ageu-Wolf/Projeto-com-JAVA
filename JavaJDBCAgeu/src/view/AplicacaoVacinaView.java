    private void btnAplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAplicarActionPerformed
  Pacientebean pacienteSelecionado = (Pacientebean) cbPaciente.getSelectedItem();
    Profissionalbean profissionalSelecionado = (Profissionalbean) cbProfissional.getSelectedItem();
    Vacinabean vacinaSelecionada = (Vacinabean) cbVacina.getSelectedItem();
    
    if (pacienteSelecionado == null || profissionalSelecionado == null || vacinaSelecionada == null) {
        JOptionPane.showMessageDialog(this, "Por favor, selecione paciente, profissional e vacina.");
        return;
    }
    
    int idPaciente = pacienteSelecionado.getIdPaciente();
    int idProfissional = profissionalSelecionado.getIdProfissional();
    int idVacina = vacinaSelecionada.getIdVacina();
    String dataAplicacao = jFormattedTextFieldDataAplicacao.getText();
    String local = jTextFieldLocal.getText();

    AplicarVacinaController controller = new AplicarVacinaController();
    boolean sucesso = controller.create(idProfissional, idPaciente, idVacina, dataAplicacao, local);

    if (sucesso) {
        JOptionPane.showMessageDialog(this, "Vacina aplicada com sucesso!");
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao aplicar vacina!", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnAplicarActionPerformed

