    private void getListaPacientes(){
        PacienteController pacienteController = new PacienteController();
        
        for (Pacientebean p : pacienteController.read()) {
            cbPaciente.addItem(p);
        }
    }
    private void getListaProfissionais() {
    ProfissionalController profissionalController = new ProfissionalController();
    for (Profissionalbean b : profissionalController.read()) {
        cbProfissional.addItem(b);
    }
}
    private void getListaVacinas() {
    VacinaController vacinaController = new VacinaController();
    for (Vacinabean v : vacinaController.read()) {
        cbVacina.addItem(v);
    }
}
        
